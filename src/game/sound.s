
.section .game.text
.global audio_loop

audio_loop:
	pushq	%r15 

	movq	 sound_index, %r15 
	movb     music(%r15), %dil

	cmpb	 $255,%dil
	jz 		 ignore
	cmpb     $254, %dil
	jz 		 note_off
	
	play_midi_note:
		shl 	$1, %dil 
		movzx   %dil, %rdi 
		movw	midi_note_to_freq_table(%rdi), %di
		// call    muteSpeaker 

		call 	playFrequency
		call    unmuteSpeaker
		jmp 	ignore
	
	note_off:
		movq	$5,%rdi 
		call    log_numq

		call 	muteSpeaker
	ignore:

	incq	%r15 
	cmpq	%r15,music_size
	jne     1f
	movq	$0, sound_index

	1:

	movq	%r15,sound_index

	popq	%r15
	
	ret 


